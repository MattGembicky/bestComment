tabs = int(input("Enter a number of tabs (2 spaces). Every line starts with that: \n"))
word = input("Type a word to get something amazing: \n")

def build(word):
    no_support = set()
    res = []
    for line in range(8):
        res.append("//  ")
        for char in word:
            if char in letters:
                res[line] += "  " + letters[char][line]
            else:
                no_support.add(char);
        res[line] += "    //\n"
    res.append("//" + "".join([" " for i in range(len(res[0]) - 5)]) + "//\n")
    res.append(res[-1])
    res.insert(0, res[-1])
    res.append("/" * (len(res[0]) - 1) + "\n")
    res.insert(0, res[-1])
    for i in range(13):
        res[i] = "".join(["  " for i in range(tabs)]) + res[i]
    if(no_support):
        print("not supported characters: ", no_support)
    return "".join(res)

file = open(word + ".txt", "w")
file.write(build(word))
file.close()

